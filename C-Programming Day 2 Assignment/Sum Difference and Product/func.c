int int_sum(int num1, int num2)
{
    int sum;
    sum=num1+num2;
    return sum;
}
int int_difference(int num1, int num2)
{
    int diff;
    diff=num1-num2;
    return diff;
}
int int_product(int num1, int num2)
{
    int pro;
    pro=num1*num2;
    return pro;
}
